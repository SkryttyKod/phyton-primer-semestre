# Escribe un programa que le pregunte al usuario su nombre y edad, 
# y luego calcule e imprima cuántos años tendrá en 20 años. 
# El programa debe imprimir una cadena de la siguiente forma:

# "Hola mi nombre es [nombre], tengo [edad] años y en 20 años tendré [total] años"

# 𝕀𝕟𝕚𝕔𝕚𝕠 𝕕𝕖𝕝 𝕣𝕖𝕥𝕠

nombre = input("¿Cual es tu nombre?\n")
edad = input("¿Cual es tu edad?\n")
total = 20 + int(edad)

print(f"Hola mi nombre es {nombre}, tengo {edad} años y en 20 años tendre {total} años")

